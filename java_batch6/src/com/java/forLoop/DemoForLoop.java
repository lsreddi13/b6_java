package com.java.forLoop;

public class DemoForLoop {

	public static void main(String[] args) {

		int n = 5;
		// for loop
		for (int i = 1; i <= n; i++) {
			System.out.println("Java is fun");
		}

		String[] emp = { "vishnu", "archana", "Sandya" };
		for (String empName : emp) {
			System.out.println(empName);
			
			
		}
	}

}
